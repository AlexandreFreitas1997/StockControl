    private void btnExcluirDadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirDadosActionPerformed
        try {
            String txt = String.valueOf(TabelaDados.getModel().getValueAt(TabelaDados.getSelectedRow() ,0));
            int idSelecionado = Integer.parseInt(txt);
            int resp = JOptionPane.showConfirmDialog(Principal.inicio, "Deseja excluir este registro?", "Confirmação de Operação", 0, 2);
            int resp1 = 9;
            if(resp == 0)
                resp1 = JOptionPane.showConfirmDialog(Principal.inicio, "Deseja realmente excluir este registro? Após confirmação não será possivel resgatar os dados excluidos!", "Confirmação de Operação", 0, 2);
                if(resp1 == 0)
                    ControlDados.excluir(idSelecionado);
        } catch (ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(Principal.inicio, "Por favor selecione uma linha da tabela!", "Erro", 0);
        }
    }//GEN-LAST:event_btnExcluirDadosActionPerformed

